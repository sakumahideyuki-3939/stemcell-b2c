<h2>違いは「取り出し方」と「含まれ方」です</h2>
<p>
  幹細胞由来の素材は、大きく「上清液（培養上清）」と「Lysate（濾液・生搾り）」に分けられます。
  この2つの違いは、成分の優劣ではなく、取り出し方と含まれる成分の範囲の違いです。
</p>

<h2>上清液は「分泌物中心」のアプローチ</h2>
<p>
  上清液は、幹細胞を培養する過程で細胞が外に分泌した成分を回収したものです。
  成長因子やサイトカインなど、細胞が「外に出した」成分が中心となります。
  細胞そのものは含まれず、品質管理がしやすいという特徴があります。
</p>

<h2>生搾り（濾液）は「破砕→濾過→管理」という設計</h2>
<p>
  一方、Lysate（生搾り・濾液）は、幹細胞を物理的に破砕し、
  細胞内部に含まれる成分まで回収した上で、濾過・精製したものです。
  上清液では回収しきれない細胞内成分も対象に含まれます。
</p>
<p>
  確認すべきポイントは以下の3点です。
</p>
<ul>
  <li><strong>破砕の方法</strong> — どのような物理的手法で細胞を処理しているか</li>
  <li><strong>濾過の基準</strong> — どのレベルまで濾過・精製しているか</li>
  <li><strong>品質管理</strong> — ロットごとの検査項目と基準はどうなっているか</li>
</ul>

<h2>言葉だけで安心せず、「工程」と「説明」を確認する</h2>
<p>
  「上清液」も「Lysate」も、名前だけで品質は判断できません。
  同じ名称でも、製造元によって工程や品質基準は異なります。
  重要なのは、名称ではなく、実際にどのような工程を経て作られているか、
  その説明がどこまで透明に開示されているかです。
</p>

<h2>検討しやすい人と、慎重に考えたい人</h2>
