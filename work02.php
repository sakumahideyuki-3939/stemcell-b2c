<h2>失敗の多くは確認不足から起きます</h2>
<p>
  幹細胞治療で後悔するケースには、共通するパターンがあります。
</p>
<ul>
  <li><strong>期待値だけで決めた</strong> — 「すごそう」という印象で始めて、想定と違う結果に不満を感じる</li>
  <li><strong>総額が見えていなかった</strong> — 初回費用だけで判断し、追加費用や継続コストを把握していなかった</li>
  <li><strong>通院条件を考えていなかった</strong> — 距離・頻度・スケジュールが現実的でなく、途中で通えなくなった</li>
</ul>

<h3>目的が「ひとことで」説明できるか</h3>
<ul>
  <li>自分が何のために検討しているか、一文で言えるか</li>
  <li>その目的に対して、幹細胞治療が唯一の選択肢ではないと理解しているか</li>
  <li>期待する結果と、現実的に見込める範囲のギャップを確認したか</li>
</ul>

<h3>医療機関が「断定せずに」説明してくれるか</h3>
<ul>
  <li>「必ず効きます」「絶対安全です」と言い切っていないか</li>
  <li>効果には個人差があることを、具体的な条件付きで説明しているか</li>
  <li>リスクや副作用について、事前に説明があるか</li>
  <li>質問に対して、はぐらかさずに回答してくれるか</li>
  <li>「分からないこと」「まだ確認されていないこと」を正直に伝えてくれるか</li>
</ul>

<h3>"何かあった時"の対応が明確か</h3>
<ul>
  <li>副作用や想定外の反応が出た場合の連絡先・対応フローが明示されているか</li>
  <li>施術後のフォローアップ体制（検診・相談窓口）があるか</li>
  <li>途中で中止した場合の費用精算ルールが説明されているか</li>
  <li>同意書の内容を事前に確認できるか</li>
  <li>同意書にサインする前に、十分な検討時間が与えられるか</li>
</ul>

<h3>"最終的にいくらか"が説明されているか</h3>
<ul>
  <li>初回費用だけでなく、推奨される回数と総額の目安が提示されているか</li>
  <li>追加費用が発生する条件（検査費、麻酔費、薬代等）が明記されているか</li>
  <li>途中解約や回数変更時の費用ルールが説明されているか</li>
  <li>支払い方法やローン条件が事前に確認できるか</li>
  <li>「キャンペーン価格」の場合、通常価格との差額と条件が明確か</li>
</ul>

<h3>続けられる前提があるか</h3>
<ul>
  <li>通院頻度と期間が、自分の生活に合っているか</li>
  <li>自宅や職場からの距離・交通手段が現実的か</li>
  <li>予約の取りやすさ（混雑状況、キャンセル対応）を確認したか</li>
  <li>長期的な費用負担が、家計に無理のない範囲か</li>
  <li>途中で方針を変えたい場合の柔軟性があるか</li>
</ul>

<h2>レッドフラッグ（避けた方がいいサイン）</h2>
<p>
  以下に該当する場合は、慎重に判断することをおすすめします。
</p>
<ul>
  <li>「今日決めないと枠が埋まります」など、即決を迫られる</li>
  <li>リスクや副作用についての説明が一切ない</li>
  <li>費用の内訳を聞いても、明確に回答してもらえない</li>
  <li>同意書を読む時間を十分に与えられない</li>
  <li>質問すると不機嫌になる、または「信じてください」と言われる</li>
</ul>

<h2>受診用質問リスト</h2>
<p>
  受診時にそのまま使える質問テンプレートです。
</p>
<ul>
  <li>この治療で期待できることと、期待しすぎない方がよいことを教えてください。</li>
  <li>リスクや副作用として、どのようなものが報告されていますか？</li>
  <li>推奨される施術回数と、総額の目安を教えてください。</li>
  <li>追加費用が発生する条件があれば、事前に教えてください。</li>
  <li>施術後に何かあった場合の連絡先と対応フローを教えてください。</li>
  <li>途中で中止した場合の費用精算はどうなりますか？</li>
  <li>同意書の内容を、事前に持ち帰って確認することは可能ですか？</li>
</ul>

