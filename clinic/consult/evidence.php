<p>
  アプローチCの「幹細胞濾液（Stem Cell Lysate）」は、細胞を超音波や凍結融解で物理的に破砕し、
  0.1〜0.45&micro;mのフィルターで濾過することで、細胞核（DNA）やミトコンドリアを除去しつつ、
  通常の上清液には含まれない細胞内タンパク質を抽出する技術です。
</p>
<p>
  従来の培養上清液が「果実を水につけて染み出した水」だとすれば、
  幹細胞濾液は「果実をすり潰して濾したジュース」に例えられます。
  細胞に由来する成分をより高濃度で採取することが可能な技術として研究されています。
</p>

