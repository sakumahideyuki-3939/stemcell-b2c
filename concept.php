<h2>未利用＝価値がある、ではありません。未利用＝まだ整理されていない領域、です</h2>
<p>
  「これまで使われていなかった成分」と聞くと、隠れた価値があるように感じるかもしれません。
  しかし、未利用であること自体は「優れている証拠」ではなく、
  単に「これまでの方法では回収対象に含まれていなかった」ということです。
</p>
<p>
  重要なのは、その成分が回収された後に、どのような品質管理と検証を経ているかです。
  「未利用だから貴重」ではなく、「未利用だったものを、どう扱っているか」で判断する必要があります。
</p>

<h2>違いは"どこまでを回収対象にしているか"</h2>
<p>
  従来の培養上清液は、幹細胞が培養中に外へ分泌した成分を回収する方法です。
  一方、物理的破砕（Lysate）は、細胞膜を壊して内部の成分まで取り出すアプローチです。
</p>
<p>
  この違いは「どちらが上か」ではなく、「回収範囲が異なる」ということです。
  上清には上清の特性があり、Lysateには細胞内成分が含まれるという特性があります。
  自分の目的に対してどちらが適しているかは、製品の詳細や医師の見解を確認して判断する必要があります。
</p>

<h2>検討するなら、ここだけは確認すると安心です</h2>
<p>
  物理的破砕による製品を検討する際、以下の4点を確認しておくと判断がしやすくなります。
</p>
<ul>
  <li><strong>原料の由来</strong> — どのような組織（脂肪・骨髄・臍帯など）から採取された幹細胞か</li>
  <li><strong>製造工程の開示</strong> — 破砕・濾過・精製のプロセスがどこまで説明されているか</li>
  <li><strong>品質管理の基準</strong> — ロットごとの検査、無菌試験、保存条件などが明示されているか</li>
  <li><strong>根拠の範囲</strong> — 特許や研究データがある場合、それが「何を示しているか」が説明されているか</li>
</ul>

<h2>「キレ」は体験談になりやすい。だから判断軸を分けます</h2>
<p>
  「使ってみたら違った」「実感があった」という体験談は、参考にはなりますが、
  それだけで製品の品質や安全性を判断することはできません。
</p>
<p>
  体験談は個人の感想であり、条件や状態が異なれば結果も変わります。
  だからこそ、「体感の話」と「製造・品質・根拠の話」は分けて考える必要があります。
  感覚的な評価と、客観的に確認できる情報を混ぜないことが、後悔しない判断の基本です。
</p>

<h2>"根拠"は、信じるためではなく、確認するために見る</h2>
<p>
  特許、論文、学会発表――これらは「この製品はすごい」と信じるための材料ではありません。
  「どこまでが確認されていて、どこからが未確定か」を把握するための手がかりです。
</p>
<p>
  根拠の有無よりも、「根拠をどう扱っているか」に注目してください。
  都合のよい部分だけを強調していないか、限界や条件も併せて説明されているか。
  その姿勢が、信頼できる情報源かどうかの判断基準になります。
</p>

