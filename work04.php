<h2>自由診療の費用は「施術代＋周辺費用＋継続コスト」</h2>
<p>
  幹細胞治療は自由診療のため、料金設定は医療機関によって異なります。
  施術代だけを見て判断すると、実際の支払額とのギャップに驚くことがあります。
  費用は以下の3層で構成されています。
</p>
<ul>
  <li><strong>施術代</strong> — 1回あたりの基本費用</li>
  <li><strong>周辺費用</strong> — 初診料、検査費、麻酔費、薬代、アフターケア費など</li>
  <li><strong>継続コスト</strong> — 推奨回数分の総額、メンテナンス施術、通院の交通費など</li>
</ul>

<h2>「安く見えるプラン」が高くなるパターン</h2>
<p>
  初回価格が安くても、以下の追加要素で総額が膨らむケースがあります。
</p>
<ul>
  <li><strong>初回限定価格</strong> — 2回目以降は通常価格に戻り、総額が大幅に上がる</li>
  <li><strong>必須オプション</strong> — 施術に必要な検査や処置が別料金として加算される</li>
  <li><strong>回数縛り</strong> — 効果を出すために最低○回のコース契約が前提</li>
  <li><strong>解約条件</strong> — 途中解約時に違約金や手数料が発生する</li>
  <li><strong>メンテナンス費用</strong> — 効果維持のための定期施術が別途必要</li>
</ul>

<h2>この5つを聞けば、ほぼ総額が読めます</h2>
<p>
  受診前に以下の質問をするだけで、費用の全体像が把握できます。
</p>
<ul>
  <li>推奨される施術回数と、1回あたりの費用を教えてください。</li>
  <li>施術代以外に発生する費用（初診料、検査費、薬代等）の一覧を教えてください。</li>
  <li>推奨コースを最後まで受けた場合の総額の目安を教えてください。</li>
  <li>途中で中止・回数変更した場合の費用精算ルールを教えてください。</li>
  <li>効果維持のためのメンテナンス施術がある場合、その費用と頻度を教えてください。</li>
</ul>

<h2>高い＝良い、ではなく「何が含まれているか」</h2>
<p>
  高額な治療が必ずしも高品質とは限りません。
  費用に見合う価値があるかどうかは、以下の観点で確認してください。
</p>
<ul>
  <li>費用に含まれるサービスの範囲（施術・検査・フォロー）が明確か</li>
  <li>使用する素材の品質管理基準が説明されているか</li>
  <li>施術後のフォローアップが費用に含まれているか</li>
  <li>他院と比較した際に、価格差の理由が説明できるか</li>
</ul>

<h2>費用の正解は、あなたの条件で決まる</h2>
<p>
  「相場はいくらですか」という質問に対して、一律の正解はありません。
  費用の判断基準は、以下の条件で決まります。
</p>
<ul>
  <li><strong>予算の上限</strong> — 無理なく支払える総額を先に決める</li>
  <li><strong>通院の現実性</strong> — 交通費・時間コストを含めて計算する</li>
  <li><strong>継続の見通し</strong> — 推奨回数を最後まで続けられるか</li>
  <li><strong>撤退ライン</strong> — 「ここまでやって変化がなければやめる」という基準を決めておく</li>
</ul>

<h2>いきなり大きな契約をしない、という選択</h2>
<p>
  不安がある場合は、最初から高額なコース契約をする必要はありません。
  まずは1回だけ受けてみて、説明の丁寧さ・施術の実感・スタッフの対応を確認し、
  その上で継続するかどうかを判断するのが、最もリスクの少ない進め方です。
</p>
<p>
  「まず1回試してみたい」と伝えて、それを快く受け入れてくれる医療機関は、
  患者の判断を尊重する姿勢があると言えます。
</p>

