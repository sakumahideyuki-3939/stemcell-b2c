<h2>この3ステップで完結します</h2>
<p>
  受診までの流れは、以下の3ステップです。
</p>
<ul>
  <li><strong>STEP 1：無料相談</strong> — 目的・条件・不安を整理し、質問リストを作成します</li>
  <li><strong>STEP 2：紹介</strong> — 条件に合う医療機関の候補をご案内します</li>
  <li><strong>STEP 3：受診</strong> — 質問リストを持って、説明を受けに行きます</li>
</ul>

<h2>「何を聞くべきか」を先に決めると、受診がラクになります</h2>
<p>
  STEP 1（無料相談）で確認する項目は以下の5つです。
</p>
<ul>
  <li><strong>目的の明確化</strong> — 何のために受診するのかを一文で言えるようにする</li>
  <li><strong>条件の整理</strong> — 通院範囲、頻度、予算を具体的に決める</li>
  <li><strong>不安の分類</strong> — 安全性・費用・適合性のどれが一番気になるかを特定する</li>
  <li><strong>質問リスト</strong> — 受診先で確認すべき質問を事前に用意する</li>
  <li><strong>撤退ラインの設定</strong> — 「ここまでやって変化がなければやめる」という基準を決めておく</li>
</ul>

<h2>"紹介"はゴールではなく、判断のスタート地点です</h2>
<p>
  紹介を受けたからといって、その医療機関で必ず受診する必要はありません。
  紹介は「候補を知る」ステップであり、最終判断はあなた自身が行います。
</p>
<h3>紹介後に進みやすい方</h3>
<ul>
  <li>質問リストが手元にあり、確認すべきことが明確な方</li>
  <li>費用の上限と撤退ラインを先に決めている方</li>
  <li>「まず1回試して判断する」というスタンスの方</li>
</ul>
<h3>もう少し慎重に考えた方がよい場合</h3>
<ul>
  <li>「紹介されたから行かなきゃ」と義務感を感じている方</li>
  <li>まだ不安が整理できていない方</li>
  <li>費用や条件を確認せずに「とりあえず行ってみよう」と思っている方</li>
</ul>

<h2>当日は「説明を受ける日」。決断の日にしなくていい</h2>
<p>
  初回受診の目的は、施術を受けることではなく、説明を聞いて判断材料を集めることです。
  以下の流れを頭に入れておくと、当日の不安が減ります。
</p>
<ul>
  <li><strong>STEP 1：受付・問診</strong> — 基本情報の確認と、現在の状態のヒアリング</li>
  <li><strong>STEP 2：説明</strong> — 治療内容・期待できること・リスクの説明を受ける</li>
  <li><strong>STEP 3：質問</strong> — 事前に準備した質問リストをもとに確認する</li>
  <li><strong>STEP 4：検討</strong> — その場で決めず、持ち帰って検討する時間を確保する</li>
  <li><strong>STEP 5：判断</strong> — 納得できたら次回の予約を入れる。納得できなければ見送る</li>
</ul>

<h2>このままコピペで使えます</h2>
<p>
  受診当日に確認すべき質問テンプレートです。
</p>
<h3>費用について</h3>
<ul>
  <li>推奨される施術回数と総額の目安を教えてください。</li>
  <li>施術代以外に発生する費用の一覧を教えてください。</li>
  <li>途中で中止した場合の費用精算ルールを教えてください。</li>
</ul>
<h3>安全性について</h3>
<ul>
  <li>施術後に起こりうる反応や副作用を教えてください。</li>
  <li>何かあった場合の連絡先と対応フローを教えてください。</li>
</ul>
<h3>期待値について</h3>
<ul>
  <li>この治療で期待できることと、期待しすぎない方がよいことを教えてください。</li>
  <li>効果の実感に個人差がある場合、どのような要因が影響しますか？</li>
</ul>

<h2>後悔する人の共通点は「条件を見ずに気持ちで決めた」</h2>
<p>
  以下のサインに心当たりがある場合は、一度立ち止まって再検討することをおすすめします。
</p>
<ul>
  <li>「今日決めないと枠が埋まる」と言われて焦っている</li>
  <li>費用の総額を確認せずに契約しようとしている</li>
  <li>質問したいことがあるのに、聞きづらい雰囲気を感じている</li>
  <li>「みんなやっている」「口コミが良い」だけで安心している</li>
  <li>同意書を読まずにサインしようとしている</li>
</ul>

<h2>段階式：説明→1回→振り返り→継続判断</h2>
<p>
  最もリスクの少ない進め方は、段階的に判断することです。
</p>
<ul>
  <li><strong>説明を受ける</strong> — まず話を聞き、質問リストで確認する</li>
  <li><strong>1回だけ試す</strong> — いきなりコース契約せず、まず1回受けてみる</li>
  <li><strong>振り返る</strong> — 説明の丁寧さ、施術の実感、スタッフの対応を評価する</li>
  <li><strong>継続を判断する</strong> — 納得できたら継続、そうでなければ見送るか別の候補を検討する</li>
</ul>

